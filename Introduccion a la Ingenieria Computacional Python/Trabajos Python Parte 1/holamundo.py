# Operaciones aritmeticas con variables
a = 16
b = 20

suma = a + b
resta = a - b
multi = a * b
divi = a / b

print(a, " + ", b, " = ",suma)
print(a, " - ", b, " = ", resta)
print(a, " * ", b, " = ", multi)
print(a, " / ", b, " = ", divi)

# Cuadrado de a
cuadrado = a**2
print(a, " ^ 2 = ", cuadrado)

# Cubo de a
cubo = a**3
print(a, " ^ 3 = ", cubo)

# Raiz de a
raiz = a**(1/2)
print(a, " ^ 1/2 = ", raiz)